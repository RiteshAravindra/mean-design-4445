document.querySelector(".sort").addEventListener("change",handlefilter)
function handlefilter(){
  let selected=document.querySelector(".sort").value
  if(selected=="All"){
    append(data)
    // console.log(data)
  }else if(selected=="HP"){
    data.sort(function(a,b){
      return b.price-a.price
    })
    append(data)
    // console.log(data)
  }
  else if(selected=="LP"){
    data.sort(function(a,b){
      return a.price-b.price
    })
    append(data)
    // console.log(data)
  }
}

function append(data){
    let length= document.getElementById("length")
    let products= document.getElementById("products")
    products.innerText=""
    length.innerText="Merch Station  -  "+data.length+" items"
    data.forEach(function(el){
        // console.log("inside function")
        let div= create('div')
        // div.style.backgroundColor="red"
        // div.style.height="100px"
        // div.style.width="100px"
        // div.style.display="flex"
        div.setAttribute("class", "product")
        
        let img = create('img')
        img.src=el.img1
        let title = create('h5')
        title.innerText=el.title
        let price= create('p')
        price.innerText="Rs."+el.price
        let centre=create('center')
        let pop = create('div')
        pop.setAttribute("id",'pop')
        // pop.addEventListener("click",function(el){
        //   localStorage.setItem("cart",JSON.stringify(productData))
        //   alert ("Item Added To Cart")
        // })
        let text= create('p')
        let center=create('center')
        center.append(text)
        div.addEventListener("mouseover", function(){
          overElem(div,center,pop)
        })
        div.addEventListener("mouseout", function(){
          outElem(div,center,pop)
        })
        div.addEventListener("click", function(){
          // console.log(el)
          localStorage.setItem("product",JSON.stringify(el))
          window.location.href="product.html"
        })
          // div.style.height="10px"
          // console.log("am inside")
        
        div.style.cursor="pointer"
        

        centre.append(img,title,price)
        div.append(centre)
        products.append(div)
    })
}
